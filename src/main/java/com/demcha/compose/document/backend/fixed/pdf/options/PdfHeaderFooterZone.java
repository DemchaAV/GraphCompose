package com.demcha.compose.document.backend.fixed.pdf.options;

/**
 * Identifies whether repeating PDF chrome belongs to the page header or footer.
 */
public enum PdfHeaderFooterZone {
    /**
     * Repeating chrome at the top of each page.
     */
    HEADER,
    /**
     * Repeating chrome at the bottom of each page.
     */
    FOOTER
}
