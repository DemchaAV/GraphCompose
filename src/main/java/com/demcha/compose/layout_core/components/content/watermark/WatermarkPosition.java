package com.demcha.compose.layout_core.components.content.watermark;

/**
 * Predefined positions for watermark placement on a page.
 *
 * @author Artem Demchyshyn
 */
public enum WatermarkPosition {

    /** Centered on the page. */
    CENTER,

    /** Placed at the top-left corner. */
    TOP_LEFT,

    /** Placed at the top-right corner. */
    TOP_RIGHT,

    /** Placed at the bottom-left corner. */
    BOTTOM_LEFT,

    /** Placed at the bottom-right corner. */
    BOTTOM_RIGHT,

    /** Tiled across the entire page with even spacing. */
    TILE
}
