package com.demcha.layout;

/**
 * Alignment options for positioning elements inside a container.
 */
public enum Align {
    /**
     * Align elements to the top edge (in horizontal layout) or left edge (in vertical layout).
     */
    TOP,

    /**
     * Align elements to the vertical or horizontal center.
     */
    CENTER,
    LEFT,
    RIGHT,

    /**
     * Align elements to the text baseline (mainly for horizontal layout with text).
     */
    BASELINE,

    /**
     * Align elements to the bottom edge (in horizontal layout) or right edge (in vertical layout).
     */
    BOTTOM
}

